    #region Test Helpers

    private static string GenerateSingle(
        string fileName,
        string typeName,
        string key,
        string namespaceName = "MyApp")
    {
        var entries = new List<XamlEntry> { new XamlEntry(typeName, key) };
        var parseResult = new XamlParseResult(fileName, entries);
        return CSharpCodeGenerator.Generate(parseResult, namespaceName);
    }

    /// <summary>
    /// Counts occurrences of <paramref name="needle"/> in the section of
    /// <paramref name="source"/> that starts after <paramref name="withinClass"/>.
    /// Matches are constrained to word boundaries so "Color" does not match "Colors".
    /// </summary>
    private static int CountOccurrences(string source, string needle, string withinClass)
    {
        // Find the class declaration with a word-boundary check: the character immediately
        // after the search string must not be a letter/digit/underscore.
        int start = -1;
        int searchFrom = 0;
        while (true)
        {
            int idx = source.IndexOf(withinClass, searchFrom, StringComparison.Ordinal);
            if (idx < 0) break;
            int afterMatch = idx + withinClass.Length;
            bool atWordBoundary = afterMatch >= source.Length
                                  || (!char.IsLetterOrDigit(source[afterMatch]) && source[afterMatch] != '_');
            if (atWordBoundary) { start = idx; break; }
            searchFrom = idx + 1;
        }
        if (start < 0) return 0;

        // Find the opening brace of this class.
        var braceStart = source.IndexOf('{', start);
        if (braceStart < 0) return 0;

        // Walk forward to find the matching closing brace (depth tracking).
        int depth = 0;
        int end = braceStart;
        for (int i = braceStart; i < source.Length; i++)
        {
            if (source[i] == '{') depth++;
            else if (source[i] == '}')
            {
                depth--;
                if (depth == 0) { end = i; break; }
            }
        }

        var section = source[braceStart..end];
        int count = 0;
        int pos = 0;
        while ((pos = section.IndexOf(needle, pos, StringComparison.Ordinal)) >= 0)
        {
            count++;
            pos += needle.Length;
        }
        return count;
    }

    #endregion
